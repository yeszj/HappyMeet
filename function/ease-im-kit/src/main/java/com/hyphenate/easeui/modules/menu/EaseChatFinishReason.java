package com.hyphenate.easeui.modules.menu;

public enum EaseChatFinishReason {
    /**
     * \~english
     * Chat ended because group was destroyed.
     */
    onGroupDestroyed,
    /**
     * \~english
     * Chat ended because user was removed from group.
     */
    onGroupUserRemoved,
    /**
     * \~english
     * Chat ended because chatroom was destroyed.
     */
    onChatRoomDestroyed,
    /**
     * \~english
     * Chat ended because user was removed from chatroom.
     */
    onChatRoomUserRemoved
}
